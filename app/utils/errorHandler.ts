// エラーハンドリング用の関数

// ステータスコードに応じてエラーメッセージと次の行動を返す
export function getErrorMessage(status: number): string {
  switch (status) {
    case 400:
      return '不正なリクエストです。リクエスト内容を確認してください。';
    case 401:
      return '無効なAPIトークンです。トークンを確認し、再試行してください。';
    case 403:
      return 'アクセスが禁止されています。権限を確認してください。';
    case 404:
      return 'リソースが見つかりません。URLを確認してください。';
    case 500:
      return 'サーバーエラーが発生しました。しばらくしてから再試行してください。';
    case 502:
      return '不正なゲートウェイです。ネットワーク接続を確認してください。';
    case 503:
      return 'サービスが利用できません。しばらくしてから再試行してください。';
    case 504:
      return 'ゲートウェイタイムアウトです。ネットワーク接続を確認し、再試行してください。';
    default:
      return '通信で不明なエラーが発生しました。アプリ製作者にお問い合わせください。';
  }
}
